package com.haazano.lesson1;

public class TheBigBang {
    private String name;
    private Integer mass;

    public TheBigBang(String name, Integer mass) {
        this.name = name;
        this.mass = mass;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getMass() {
        return mass;
    }

    public void setMass(Integer mass) {
        this.mass = mass;
    }
}
